def is_crude(name: str) -> bool:
    """Check if a color name is considered crude.

    Args:
        name: Color name (normalized: lowercase, underscores).

    Returns:
        True if name is in crude names set.

    Example:
        >>> is_crude("puke_green")
        True
        >>> is_crude("grass_green")
        False
    """
    return name in CRUDE_NAMES
